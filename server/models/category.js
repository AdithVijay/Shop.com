const mongoose = require("mongoose")

const categorySchema = new mongoose.Schema({
    category: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
    isListed: {
      type: Boolean,
      default: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
    updatedAt: {
      type: Date,
      default: Date.now,
    },
  });

  const Category = mongoose.model("categorie", categorySchema);

  module.exports = Category